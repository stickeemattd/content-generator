            <h2>What is ADSL broadband?</h2>
            <p>
                ADSL stands for Asymmetric Digital Subscriber Line. It uses the same wires as a traditional telephone
                line, but delivers a broadband connection and landline calls at the same time.
            </p>
            <p>
                In the early 2000s this made ADSL a big improvement on dial-up internet. You could talk on the phone
