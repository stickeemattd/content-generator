            <h2>FAQs</h2>
            <h3>What can’t you do with ADSL broadband?</h3>
            <p>
                ADSL broadband has lower bandwidth than fibre or cable connections. There's a physical limit to the
                amount of data an ADSL line can transmit per second and this tops out at around 24Mbps (megabits per
                second).
            </p>
            <p>
                So the faster type of ADSL connection is just about enough to use a streaming platform like Netflix or
                make a video call. It's also more than enough for lower-bandwidth activities like checking and sending
                email.
            </p>
            <p>
                ADSL broadband is not fast enough to let several people use streaming services or download video at
                once.
            </p>
            <p>
                Symmetrical speeds are becoming a more common feature of modern broadband services, but with ADSL the
                upload speeds are always much slower than the download speeds. This means you can't:
            </p>
            <ul>
                <li>Work effectively in the cloud</li>
                <li>Be sure of a high-quality, lag-free video call</li>
                <li>Do online gaming</li>
                <li>Host a server</li>
                <li>Stream your own video content</li>
            </ul>
            <h3>Do I need any special equipment for ADSL broadband?</h3>
            <p>
                Every broadband service needs a router to connect the external network to the network in your home and
                allow multiple devices to use the same connection.
            </p>
            <p>
                ADSL also needs a modem. This is because ADSL uses traditional telephone wires, which carry an analogue
                signal. The modem does the job of converting those analogue signals into digital ones for the router to
                manage.
            </p>
            <p>You can get combined modem/routers which do both jobs.</p>
            <p>As you've read, you might need a splitter to separate the phone and broadband lines.</p>
            <p>Usually your internet provider will supply the equipment you need to access their broadband service.</p>
            <h3>What is ADSL+?</h3>
            <p>
                ADSL+ is a slightly more advanced version of ADSL. It's speedier than the original version, although
                it's still significantly slower than fibre broadband.
            </p>
            <p>
                You might get 10-11Mbps with an older ADSL connection, which jumps to over 20Mbps with ADSL+. But even
                the slowest fibre connections available will give you at least 30Mbps.
            </p>
            <p>
                The original ADSL type of connections are sometimes known as ADSL1, and ADSL+ is sometimes called ADSL2.
            </p>
            <h3>Is ADSL being discontinued?</h3>
            <p>
                Yes, ADSL broadband is being phased out and the old copper phone lines are being replaced with fibre
                optic cables.
            </p>
